package com.sabdar.android.quizapp;

public class result {
    private int marks;
    private String subject;

    public result() {
        marks = 0;
        subject = "";
    }

    public void setMarks(int marks) {
        this.marks = marks;
    }

    public int getMarks() {
        return marks;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getSubject() {
        return subject;
    }
}                                    






